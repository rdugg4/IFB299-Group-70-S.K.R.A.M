import datetime

class timeObject(object):
    date = 0

    def __init__():
        self.date = 0

    def dateNum():
        return self.date

    def getDay():
        day = int(str(self.date)[6:8])
        return day

    def getWeek():
        week = int(str(self.date)[4:6])
        return week

    def getYear():
        year = int(str(self.date)[0:4])
        return year
